package ru.practicum.shareit.booking.model;

/**
 * Перечисление статусов бронирования.
 * <p>
 * Определяет возможные статусы жизненного цикла бронирования.
 * </p>
 */
public enum Status {
    /** Бронирование ожидает подтверждения. */
    WAITING,
    /** Бронирование подтверждено владельцем. */
    APPROVED,
    /** Бронирование отклонено владельцем. */
    REJECTED,
    /** Бронирование отменено создателем. */
    CANCELLED
}
